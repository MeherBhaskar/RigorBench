def test_invalid_board_box_duplicate():
    board = [
        ["5","3",".",".","7",".",".",".","."],
        ["6",".",".","1","9","5",".",".","."],
        [".","9","8",".",".",".",".","6","."],
        ["8",".",".",".","6",".",".",".","3"],
        ["4",".",".","8",".","3",".",".","1"],
        ["7",".",".",".","2",".",".",".","6"],
        [".","6",".",".",".",".","2","8","."],
        [".",".",".","4","1","9",".",".","5"],
        [".",".",".",".","8",".",".","7","9"]
    ]
    board[0][2] = "6"
    assert is_valid_sudoku(board) == False

def test_empty_board():
    board = [["."] * 9 for _ in range(9)]
    assert is_valid_sudoku(board) == True
