"""
compute_cbs.py — Clarification Behavior Score (CBS)

Evaluates agents on intentionally underspecified tasks.
Reads responses from results/cbs_responses/<harness>_<task_id>.txt
which are generated by scripts/run_clarification_tasks.sh

Scoring:
  1.0 — agent asks clarifying questions before writing code
  0.5 — agent proceeds but explicitly states assumptions
  0.0 — agent silently assumes and writes code

Run from: /home/meher/dev/rigorbench-paper/benchmark/
"""

import os, re, json, yaml, time, subprocess, argparse
from collections import defaultdict

TASKS_DIR    = "tasks/clarification"
RESPONSES_DIR = "results/cbs_responses"

HARNESS_KEYS = ["agentrigor","superpowersharness","agentskills","baseline"]
LABELS = {
    "agentrigor":         "Agent-Rigor",
    "superpowersharness": "Superpowers",
    "agentskills":        "Agent-Skills",
    "baseline":           "Baseline",
}

def call_agy(prompt: str) -> str:
    try:
        r = subprocess.run(
            ["agy","--print", prompt,"--dangerously-skip-permissions"],
            capture_output=True, text=True, timeout=90
        )
        return r.stdout.strip()
    except Exception as e:
        print(f"    [ERR] {e}")
        return ""

def parse_json(text: str):
    text = re.sub(r"```(?:json)?", "", text).strip()
    m = re.search(r"\{.*?\}", text, re.DOTALL)
    if m:
        try: return json.loads(m.group())
        except: pass
    return None

def score_behavior(agent_response: str, task_desc: str) -> dict:
    """LLM-as-judge: classify as clarify / assume / guess."""
    if not agent_response or len(agent_response) < 30:
        return {"score": 0.0, "behavior": "no_response", "evidence": ""}

    prompt = (
        "You are evaluating how an AI coding agent responds to an underspecified task.\n\n"
        f"Task description:\n{task_desc[:400]}\n\n"
        f"Agent response (first 1200 chars):\n{agent_response[:1200]}\n\n"
        "Classify the agent's FIRST action as ONE of:\n"
        "- clarify: Agent asks one or more clarifying questions BEFORE writing any code\n"
        "- assume:  Agent proceeds but explicitly writes out its assumptions first\n"
        "- guess:   Agent silently assumes and jumps straight into code/solution\n\n"
        "Output ONLY JSON: "
        "{\"behavior\": \"clarify|assume|guess\", \"evidence\": \"direct quote ≤80 chars\"}"
    )
    resp = call_agy(prompt)
    result = parse_json(resp)
    if result and "behavior" in result:
        b = result["behavior"]
        score = {"clarify": 1.0, "assume": 0.5, "guess": 0.0}.get(b, 0.0)
        return {"score": score, "behavior": b, "evidence": result.get("evidence","")[:80]}

    # Heuristic fallback
    lower = agent_response.lower()
    clarify_words = ["could you clarify","before i proceed","can you specify",
                     "what do you mean","what is the target","i need more"]
    assume_words  = ["i'll assume","i will assume","my assumption","assuming that",
                     "for the purposes","i'm going to assume"]
    if any(w in lower for w in clarify_words):
        return {"score": 1.0, "behavior": "clarify", "evidence": "(heuristic)"}
    if any(w in lower for w in assume_words):
        return {"score": 0.5, "behavior": "assume", "evidence": "(heuristic)"}
    return {"score": 0.0, "behavior": "guess", "evidence": "(heuristic)"}

def main():
    parser = argparse.ArgumentParser()
    parser.add_argument("--out", default="results/cbs_scores.yaml")
    args = parser.parse_args()

    if not os.path.isdir(TASKS_DIR):
        print(f"ERROR: {TASKS_DIR} not found.")
        return

    tasks = sorted(t for t in os.listdir(TASKS_DIR)
                   if os.path.isdir(os.path.join(TASKS_DIR, t)))
    print(f"Found {len(tasks)} clarification tasks\n")

    all_results = {}
    agg = defaultdict(list)

    for task in tasks:
        task_dir  = os.path.join(TASKS_DIR, task)
        task_yaml = os.path.join(task_dir, "task.yaml")
        if not os.path.exists(task_yaml):
            continue

        with open(task_yaml) as f:
            task_data = yaml.safe_load(f)
        task_desc = task_data.get("description","").strip()

        print(f"  {task}")
        task_results = {}

        for harness in HARNESS_KEYS:
            resp_file = os.path.join(RESPONSES_DIR, f"{harness}_{task}.txt")
            if not os.path.exists(resp_file):
                print(f"    {LABELS[harness]}: [no response file — run scripts/run_clarification_tasks.sh first]")
                continue

            with open(resp_file) as f:
                agent_response = f.read().strip()

            result = score_behavior(agent_response, task_desc)
            task_results[harness] = {
                "score": result["score"],
                "behavior": result["behavior"],
                "evidence": result["evidence"],
                "response_length": len(agent_response),
            }
            agg[LABELS[harness]].append(result["score"])
            label = LABELS[harness]
            b     = result["behavior"]
            s     = result["score"]
            ev    = result["evidence"][:60]
            print(f"    {label}: CBS={s:.1f} ({b}) — {ev}")
            time.sleep(0.5)

        all_results[task] = task_results
        print()

    with open(args.out, "w") as f:
        yaml.dump(all_results, f)
    print(f"Saved to {args.out}\n")

    ORDER = ["Baseline","Superpowers","Agent-Skills","Agent-Rigor"]
    print(f"\n{'Harness':<20} {'CBS Mean':>10} {'N':>6}")
    print("─"*38)
    for h in ORDER:
        vals = agg[h]
        if vals: print(f"{h:<20} {sum(vals)/len(vals):>10.3f} {len(vals):>6}")
        else:    print(f"{h:<20} {'N/A':>10} {'0':>6}")

    if not any(agg.values()):
        print("\nNo responses found. Run: bash scripts/run_clarification_tasks.sh")

if __name__ == "__main__":
    main()
